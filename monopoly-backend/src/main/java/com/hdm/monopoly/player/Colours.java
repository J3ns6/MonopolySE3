package com.hdm.monopoly.player;

public class Colours {
    public String getColours(int i){
        switch(i){
            case 0: return "#f44336";
            case 1: return "#2196f3";
            case 2: return "#4caf50";
            case 3: return "#FF6F00";
            default: return null;
        }
    }
}
